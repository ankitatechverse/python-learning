# ==============================
# Python Variables Cheat Sheet
# A variable is just a name that stores a value in memory.
# Think of it like a labeled box where you can put stuff.
# =============================

# Creating variables
name = "Genius"
age = 25
height = 5.8
is_coder = True
skills = ["Python", "Laravel"]

print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is Coder?", is_coder)
print("Skills:", skills)


