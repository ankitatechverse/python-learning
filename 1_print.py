# Example print statements to demonstrate code readability
print("Hello Ankita Tech Verse")  # Prints a welcome message
print('Welcome to the world of Python programming!')  # Another message
print("'Let's explore the wonders of coding together!'")  # Quoted string
print("Here's to a journey filled with learning and growth!")  # Motivational text

# -------------------------------
# Printing different data types
# -------------------------------
print(3.14)  # Floating-point number
print(3)  # Integer
print(3 + 3)  # Arithmetic operation result

# -------------------------------
# Printing Boolean values
# -------------------------------
print(True)  # Boolean True
print(False)  # Boolean False

# -------------------------------
# Printing multiple values
# -------------------------------
print("hello ", 3)  # String with integer
print("hello", 3, "world")  # Multiple items in one print
print("hello", 3, "world", sep=" - ")  # Custom separator between values
print("hello", 3, "world")  # Regular print (default separator is space)
print("hello", str(3), "world")  # Integer converted to string before printing
print("hello", 3, "world", sep=" - ",end="!\n")  # Another print with custom separator

# -------------------------------
# Ending messages
# -------------------------------
print("End of the print statements.")  # Indicates end of prints
print("Thank you for joining me on this coding adventure!")  # Closing line